    private void operatorsAction(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_operatorsAction
        // TODO add your handling code here:
        if(edtText.getText().length() > 0){
        num1 = Double.parseDouble(edtText.getText());
        switch (evt.getActionCommand()){
            case "+":

                operator = '+';
                lblShow.setText(num1 + " + ");
                
                break;
            case "-":
                operator = '-';
                lblShow.setText(num1 + " - ");
                break;
            case "x":
                operator = 'x';
                lblShow.setText(num1 + " x ");
                break;
            case "/":
                operator = '/';
                lblShow.setText(num1 + " / ");
                break;
        }
        edtText.setText("");
    }//GEN-LAST:event_operatorsAction
}
    private void btnEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEqualActionPerformed
        // TODO add your handling code here:
        if (edtText.getText().length() > 0){
            num2 = Double.parseDouble(edtText.getText());
        } else {
            num2 = num1;
        }            
        double result = 0 ;
        if(operator != '\u0000'){
        switch (operator){
            
            case '+':
                result = num1 + num2;
                break;
            case '-':
                result = num1 - num2;
                break;
            case 'x':
                result = num1 * num2;
                break;
            case '/':
                result = num1 / num2;
                break;
        }
        operator = '\u0000';
        edtText.setText(String.valueOf(result));
        lblShow.setText(lblShow.getText()+ num2 +" =");  
        }
        else {
            edtText.setText (num2 + "");
        }
    }//GEN-LAST:event_btnEqualActionPerformed

    
    
    
    
    
